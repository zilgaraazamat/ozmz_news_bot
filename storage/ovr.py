"""Overall Rating (OVR) — чистая функция от статистики игрока.

OVR никогда не хранится и никогда не редактируется вручную — ни через
админку, ни через какой-либо другой путь. Это единственное место в
приложении, где он считается: вход — текущие games_played/goals/mvp_count
(см. storage/player_stats.py), выход — рейтинг. Один и тот же вход всегда
даёт один и тот же результат, без обращений к БД и без побочных эффектов.

Поскольку OVR не хранится, он физически не может «устареть» или разойтись
с реальной статистикой — он автоматически пересчитывается на каждый запрос
из актуальных данных. Именно так выполняется требование «OVR обновляется
автоматически при изменении статистики»: обновлять нечего, он всегда свежий.

Формула:
    OVR = 60
        + (сыгранных игр // 5)
        + (голов // 10)
        + (MVP-наград // 5)

Чтобы поменять формулу (или добавить в неё новый фактор — например, штраф
за жёлтые карточки) — правится только этот файл, все места, где OVR
показывается (Home, профиль, публичная страница игрока, админка), продолжат
работать без изменений, потому что читают уже готовое число из
get_player_stats().
"""

BASE_OVR = 60
GAMES_PER_POINT = 5
GOALS_PER_POINT = 10
MVP_PER_POINT = 5


def calculate_ovr(games_played, goals, mvp_count):
    """Чистая функция: (сыгранные игры, голы, MVP) → OVR.
    Отрицательные/некорректные значения не могут утащить рейтинг ниже базы."""
    games_played = max(0, int(games_played or 0))
    goals = max(0, int(goals or 0))
    mvp_count = max(0, int(mvp_count or 0))

    return (
        BASE_OVR
        + games_played // GAMES_PER_POINT
        + goals // GOALS_PER_POINT
        + mvp_count // MVP_PER_POINT
    )
